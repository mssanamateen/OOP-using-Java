class TypCast{
	public static void main(String[] args) {
		int marks=80;
		long lmv=marks;
		System.out.println("integer value"+marks);
		System.out.println("long value"+lmv);

		float  d=129.345;
		double inm=(double)d;
		System.out.println("double value"+d);
		System.out.println("integer value"+inm);
	}
}