import java.util.Scanner;
class Concatenate{
	public static void main(String[] args){
		String first="java";
		String second="lab";
		String complete=first.concat(second);
		System.out.println(complete);
		String name="james";
		String lname="gosling";
		String auth=name+lname;
		System.out.println(auth);
	}
}