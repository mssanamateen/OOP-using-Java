public class Primitive {
    public static void main(String[] args) {

        // Primitive data types

        int Mynumber = 97; // ranging from -2,147,483,648 to 2,147,483,647.
        short Myshort = 830; // ranging from -32,768 to 32,767.
        long Mylong = 97723323; // ranging from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807.

        double Mydouble = 7.121121;
        float Myfloat = 3.45f;

        char Mychar = 'Y';
        boolean Myboolean = true;

        byte Mybyte = 127;

        System.out.println(Mynumber);
        System.out.println(Myshort);
        System.out.println(Mylong);
        System.out.println(Mydouble);
        System.out.println(Myfloat);
        System.out.println(Mychar);
        System.out.println(Myboolean);
        System.out.println(Mybyte);
    }
}